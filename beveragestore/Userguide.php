<h1 color="red">User Guide</h1> 

<h2>For Users</h2>
<h2>For any help required during the process of using Online Beverage System please go through the following instructions -</h2>

<h2>User can log into the system using the following information-</h2>
<h3>•	1st Step - First of all users’ needs to register in the register page which is at the top right 	corner of all pages, providing all the information the web-browser asked for, Then click submit button.<br>
	•	2nd Step - Then the users need to log into the system which is also at the top right corner of the webpage using the Username and the password that they have provided in the registration form.
</h3>

<h2>User can order items using the following information-</h2>

<h3>•	1st Step - They needs to log into the system where they should have already filled the registration form.<br>
	•	2nd Step - After the successful log into the system users can easily see the add to cart button on each and every items of the beverage store.<br>
	•	3rd step - Then after clicking the Add to Cart button they can easily send the orders for the delivery.</h3>


<h2>For the admin</h2>
<h2>To add new admin following steps are to be taken-</h2>
<h3>
•	1st Step - Super admin account has already been created so he/she can access into the admin page directly after the login to the system.<br>
•	2nd Step - After going to the Admin users panel the admin can click add new admin.<br>
•	3rd step - Then fulfilling all the required information new admin can be added.
</h3>

<h2>
To add new items into the website-</h2>
<h3>
•	1st Step - After the successful login to the system by admin. <br>
•	2nd Step - There is the Option to add Items.<br>
•	3rd step - Then fulfilling all the required information new items can be added.
